#!/usr/bin/env bash
# scripts/check-structure.sh

# 📁 Path setup
ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/../src" && pwd)"
OUTPUT_FILE="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)/STRUCTURE.md"

# 🧠 Indent function (2 spaces per level)
indent() {
  local level=$1
  local i
  for ((i = 0; i < level; i++)); do
    printf "  "
  done
}

# 🔍 Collect all .ts and .tsx files (relative paths)
mapfile -t entries < <(find "$ROOT_DIR" -type f \( -name "*.ts" -o -name "*.tsx" \) | sed "s|^$ROOT_DIR/||" | sort)

# 📦 Build tree output
declare -A seen
output=""

for entry in "${entries[@]}"; do
  IFS='/' read -ra parts <<< "$entry"
  current=""
  for i in "${!parts[@]}"; do
    current+="${parts[i]}"
    key="${i}-${current}"
    if [[ -z "${seen[$key]}" ]]; then
      seen["$key"]=1
      is_file=""
      [[ "${parts[i]}" == *.* && $i -eq $((${#parts[@]} - 1)) ]] && is_file="" || is_file="/"
      output+=$(indent "$i")"- ${parts[i]}${is_file}"$'\n'
    fi
    current+="/"
  done
done

# ✍️ Write STRUCTURE.md
{
  echo "# 📁 Project Source Structure (\`src/\`)"
  echo
  echo '```text'
  echo "$output"
  echo '```'
  echo
  echo '> ✅ Generated by `scripts/check-structure.sh`'
  echo '> ℹ️ NOTE: โค้ดทั้งหมดทำงานภายใต้แนวทาง Dev to Dev แบบเข้มงวด ตรวจสอบโครงสร้างโปรเจกต์ให้เป็นระบบ หลีกเลี่ยงการแตกไฟล์นอกคำสั่งหรือ generate ที่ไม่จำเป็น'
} > "$OUTPUT_FILE"

echo "✅ STRUCTURE.md generated at: $OUTPUT_FILE"
